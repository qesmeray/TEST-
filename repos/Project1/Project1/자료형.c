#include <stdio.h>

int main() {

	//자료형 : 정보의 유형
	//기본적인 자료형은 signed형/unsigned형은 0과 양수만 표현가능
	 
	//정수형-int
	short myShort;
	int myInt;   //-21억~+21억
	unsigned int myint;   //0~42억
	long myLong;
	
	//실수형-double,float
	//정밀도의 차이(소수점 표현범위차이 3.14/3.1419...)
	float myfloat;   //%f
	double mydouble;   //%lf

	//문자형-char
	char mychar;


	
}